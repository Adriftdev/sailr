_sailr() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="sailr"
                ;;
            sailr,archive)
                cmd="sailr__archive"
                ;;
            sailr,deploy)
                cmd="sailr__deploy"
                ;;
            sailr,env)
                cmd="sailr__env"
                ;;
            sailr,generate)
                cmd="sailr__generate"
                ;;
            sailr,go)
                cmd="sailr__go"
                ;;
            sailr,help)
                cmd="sailr__help"
                ;;
            sailr,init)
                cmd="sailr__init"
                ;;
            sailr__env,create)
                cmd="sailr__env__create"
                ;;
            sailr__env,help)
                cmd="sailr__env__help"
                ;;
            sailr__env__help,create)
                cmd="sailr__env__help__create"
                ;;
            sailr__env__help,help)
                cmd="sailr__env__help__help"
                ;;
            sailr__help,archive)
                cmd="sailr__help__archive"
                ;;
            sailr__help,deploy)
                cmd="sailr__help__deploy"
                ;;
            sailr__help,env)
                cmd="sailr__help__env"
                ;;
            sailr__help,generate)
                cmd="sailr__help__generate"
                ;;
            sailr__help,go)
                cmd="sailr__help__go"
                ;;
            sailr__help,help)
                cmd="sailr__help__help"
                ;;
            sailr__help,init)
                cmd="sailr__help__init"
                ;;
            sailr__help__env,create)
                cmd="sailr__help__env__create"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        sailr)
            opts="-h -V --help --version init env deploy generate go archive help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sailr__archive)
            opts="-h --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sailr__deploy)
            opts="-c -n -h --context --name --help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                --context)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                -c)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                --name)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                -n)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sailr__env)
            opts="-h --help create help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sailr__env__create)
            opts="-h --help [name]"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        sailr__env__help)
            opts="create help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
